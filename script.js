function mostrarFerramentas(categoria) {
    const lista = document.getElementById("listaFerramentas");
    lista.innerHTML = "";
    ferramentas[categoria].forEach(ferramenta => {
        const item = document.createElement("li");
        item.innerHTML = `<img src="${ferramenta.imagem}" alt="${ferramenta.nome}" class="icon"> ${ferramenta.nome}`;
        item.onclick = () => mostrarDetalhes(ferramenta);
        lista.appendChild(item);
    });
    document.getElementById("ferramentas").classList.remove("hidden");
    document.getElementById("detalhes").classList.add("hidden");
}

function mostrarDetalhes(ferramenta) {
    document.getElementById("imagemFerramenta").src = ferramenta.imagem;
    document.getElementById("descricaoFerramenta").textContent = ferramenta.descricao;
    document.getElementById("linkFerramenta").href = ferramenta.link;
    document.getElementById("linkFerramenta").textContent = "Acesse aqui";
    document.getElementById("detalhes").classList.remove("hidden");
}

